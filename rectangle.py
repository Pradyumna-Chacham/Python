#Program to find area and perimeter of a rectangle
l=int(input("Enter length of rectangle:"))
b=int(input("Enter breadth of rectangle:"))
area=l*b
perimeter=2*(l+b)
print ("The area of rectangle of length",l,"and breadth",b,"is :",area)
print ("The perimeter of rectangle of length",l,"and breadth",b,"is :",perimeter)
